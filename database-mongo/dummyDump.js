// =========== RANDOM DESCRIPTION TEXT GENERATOR =======================
//Initial Response to prompt: 'Hey Ateam, can we do something fun today?
const randomElement = (array) => {
  const randomIndex = Math.floor(Math.random() * array.length);
  return array[randomIndex];
};

const intro = [
  'Hi Susan!', 'Hey girlie!', 'Hola Susana La Mala!', 'Hey Beautiful Sister!', 'Hey Suz!'
];

const body = [
  'Yes, of course. Im thinking maybe 3pm today. I can pick you up and we can go to the beach!', 'That is a great idea!! The weather is so nice out, lets do it!',
  'Ohhhh, we can go see the new blockbuster movie that just came out!',
];

const ending = [
  'How does that sound?', 'Sound good?', 'Cant wait! Love you'
];

const randomDescription = function () {
  return [randomElement(intro), randomElement(body), randomElement(ending)].join(' ');
};

